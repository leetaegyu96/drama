package dto;

public class CommentsDTO {

	private String c_mcode;
	private String c_dcode;
	private int ccode;
	private String cpw;
	private String comments;
	private float grade;
	public String getC_mcode() {
		return c_mcode;
	}
	public void setC_mcode(String c_mcode) {
		this.c_mcode = c_mcode;
	}
	public String getC_dcode() {
		return c_dcode;
	}
	public void setC_dcode(String c_dcode) {
		this.c_dcode = c_dcode;
	}
	public int getCcode() {
		return ccode;
	}
	public void setCcode(int ccode) {
		this.ccode = ccode;
	}
	public String getCpw() {
		return cpw;
	}
	public void setCpw(String cpw) {
		this.cpw = cpw;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public float getGrade() {
		return grade;
	}
	public void setGrade(float grade) {
		this.grade = grade;
	}
	
	
}
